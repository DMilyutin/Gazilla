package com.gazilla.mihail.gazillaj.utils.POJO;

public class Balances {

    private int sum;
    private int score;

    public Balances(int sum, int score) {
        this.sum = sum;
        this.score = score;
    }

    public int getSum() {
        return sum;
    }

    public int getScore() {
        return score;
    }
}
